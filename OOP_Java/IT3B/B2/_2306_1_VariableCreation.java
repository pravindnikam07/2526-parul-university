public class _2306_1_VariableCreation {
    public static void main(String[] args) {
        System.out.println("Hi Java");
    
        // creating a variabl in Java
        // Syntax
        // data_type variable_name = value; => declaration and initialization
        String name = "Rohit"; // declaration and initialization
        System.out.println(name); // output => Rohit
        
        // data_type variable_name; => declaration
        // variable_name = value;   => initialization
        String lastName; // declaration
        lastName = "Sharma";  // initialization
        System.out.println(lastName); // output => Sharma
        
        
        int i = 10;
        System.out.println(i);
        System.out.println(23);
    }
}