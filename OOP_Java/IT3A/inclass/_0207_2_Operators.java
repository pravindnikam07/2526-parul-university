public class _0207_2_Operators {
    public static void main(String[] args) {
        // Operators in Java

        // 1. Arithmetic Operators
        /*
         *   +  -  *  /  %  ++  -- (addition, subtraction, multiplication, division, modulus)
         */

         int a = 10;
         int b = 40;

         int sum = a + b; // addition
         System.out.println("Addition: " + sum); // Output: Addition: 50

        int subtraction = a - b; // substraction
        System.out.println("Subtraction: " + subtraction); // Output: Subtraction: -30

       int num1 = 12;
       int num2 = 9;
       int rem = num1 % num2; // 3 
    }
}
